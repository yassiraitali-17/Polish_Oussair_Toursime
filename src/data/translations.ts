export const useTranslation = (language: Language) => {
  return {
    t: (path: string, fallback?: string) => {
      const keys = path.split('.');
      let value: any = translations[language];
      
      for (const key of keys) {
        value = value?.[key];
        if (value === undefined) {
          return fallback || path;
        }
      }
      
      return value;
    },
  };
};
